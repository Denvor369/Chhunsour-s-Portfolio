type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [locale, setLocale] = useState<Locale>(() => {
    const saved = localStorage.getItem("portfolio-locale");
    return saved === "km" || saved === "zh" ? saved : "en";
  });

  useEffect(() => {
    localStorage.setItem("portfolio-locale", locale);
    document.documentElement.lang = locale === "km" ? "km" : locale === "zh" ? "zh-CN" : "en";
    document.documentElement.dataset.locale = locale;
  }, [locale]);

  // fade the page out, swap language, fade back in
  const changeLocale = (next: Locale) => {
    if (next === locale) return;
    if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
      setLocale(next);
      return;
    }
    const root = document.documentElement;
    root.classList.add("locale-switching");
    window.setTimeout(() => {
      setLocale(next);
      window.setTimeout(() => root.classList.remove("locale-switching"), 80);
    }, 240);
  };

  const value = useMemo(
    () => ({
      locale,
      setLocale: changeLocale,
      t: (key: string) => translations[locale][key] ?? translations.en[key] ?? key,
    }),
    [locale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used inside I18nProvider");
  return context;
};

export const LanguageSwitcher = () => {
  const { locale, setLocale } = useI18n();
  const options: { value: Locale; label: string; title: string }[] = [
    { value: "en", label: "EN", title: "English" },
    { value: "km", label: "ខ្មែរ", title: "ភាសាខ្មែរ" },
    { value: "zh", label: "中文", title: "中文" },
  ];

  return (
    <div
      className="flex items-center border border-[#ffe9d9]/20 bg-[#272727]/75 p-1 backdrop-blur-sm"
      role="group"
      aria-label="Choose language"
    >
      {options.map((option) => (
        <button
          key={option.value}
          type="button"
          title={option.title}
          aria-pressed={locale === option.value}
          onClick={() => setLocale(option.value)}
          className={`min-h-11 min-w-9 px-2 py-2 text-center text-[11px] leading-none transition-all duration-200 active:scale-90 sm:min-h-0 sm:min-w-11 sm:px-3 sm:text-xs ${
            locale === option.value
              ? "bg-[#fe7f2d] text-[#272727]"
              : "text-[#ffe9d9]/65 hover:text-[#ffe9d9]"
          } ${option.value === "km" ? "min-w-12 font-kantumruy text-[13px]" : "[font-family:'OTTERO-Regular',Helvetica]"}`}
        >
          {option.label}
        </button>
      ))}
    </div>
  );
};
